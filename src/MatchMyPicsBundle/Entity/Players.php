<?php

namespace MatchMyPicsBundle\Entity;

/**
 * Players
 */
class Players
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $player;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set player
     *
     * @param string $player
     *
     * @return Players
     */
    public function setPlayer($player)
    {
        $this->player = $player;

        return $this;
    }

    /**
     * Get player
     *
     * @return string
     */
    public function getPlayer()
    {
        return $this->player;
    }
    /**
     * @var \MatchMyPicsBundle\Entity\Team
     */
    private $team;


    /**
     * Set team
     *
     * @param \MatchMyPicsBundle\Entity\Team $team
     *
     * @return Players
     */
    public function setTeam(\MatchMyPicsBundle\Entity\Team $team = null)
    {
        $this->team = $team;

        return $this;
    }

    /**
     * Get team
     *
     * @return \MatchMyPicsBundle\Entity\Team
     */
    public function getTeam()
    {
        return $this->team;
    }
}
